export default function InfoPage() {
  const { slug } = useParams<{ slug: string }>();

  const { data: page } = useQuery<any>({
    queryKey: ['page', slug],
    queryFn: () => api.get(`/content/pages/${slug}`),
    enabled: !!slug && !STATIC_PAGES[slug],
    retry: false,
  });

  const staticPage = STATIC_PAGES[slug || ''];
  const title = page?.title || staticPage?.title || slug;
  const content = page?.content || staticPage?.content || '';

  return (
    <div className="max-w-3xl mx-auto px-4 sm:px-6 py-12">
      <h1 className="text-3xl font-bold text-gray-900 mb-8">{title}</h1>
      <div className="bg-white rounded-2xl border border-gray-100 p-8 prose prose-gray max-w-none">
        {content.split('\n\n').map((paragraph: string, i: number) => (
          <div key={i} className="mb-4">
            {paragraph.split('\n').map((line: string, j: number) => (
              <p key={j} className={`${line.startsWith('•') ? 'ml-4' : ''} text-gray-700 leading-relaxed`}>
                {line}
              </p>
            ))}
          </div>
        ))}
      </div>
    </div>
  );
}
