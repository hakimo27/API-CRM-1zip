function renderMarkdown(text: string) {
  return text
    .split("\n")
    .map((line, i) => {
      if (line.startsWith("## ")) return <h2 key={i} className="text-2xl font-bold text-gray-900 mt-6 mb-3">{line.slice(3)}</h2>;
      if (line.startsWith("### ")) return <h3 key={i} className="text-lg font-semibold text-gray-800 mt-5 mb-2">{line.slice(4)}</h3>;
      if (line.startsWith("**") && line.endsWith("**")) return <p key={i} className="font-semibold text-gray-900 mb-1">{line.slice(2, -2)}</p>;
      if (line.startsWith("- ")) return <li key={i} className="text-gray-600 ml-4 list-disc">{line.slice(2)}</li>;
      if (/^\d+\. /.test(line)) return <li key={i} className="text-gray-600 ml-4 list-decimal">{line.replace(/^\d+\. /, "")}</li>;
      if (line.trim() === "") return <div key={i} className="h-2" />;
      return <p key={i} className="text-gray-600 leading-relaxed">{line}</p>;
    });
}

